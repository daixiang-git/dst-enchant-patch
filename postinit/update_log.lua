-- 补丁mod更新日志注入
-- 在本体mod的更新日志界面显示补丁mod的更新记录

local function AddPatchUpdateLog()
    -- 确保本体mod的更新日志表存在
    if not TUNING then return end
    if not TUNING.HH_UI_TEXT then return end
    if not TUNING.HH_UI_TEXT.UPDATE_VISION then return end

    -- 补丁mod的更新记录
    local patchLogs = {
        {
            ["title"] = "补丁mod - 2026-03-27",
            ["desc"] = [[
            --------新增功能--------
            【生命附魔石随机获取】
              新增配置开关，可控制生命附魔石是否进入正常随机池
              默认开启
              开启后，初/中/高/极生命附魔石会进入正常随机池
            ]]
        },
        {
            ["title"] = "补丁mod - 2026-03-25",
            ["desc"] = [[
            --------修复--------
            【掉率系统】
              开启后，额外掉落现在会优先放入玩家背包
              只有背包放不下时才会掉在地上

            【怪物词条数量配置】
              修复树精、大象等新生成怪物
              初始词条不会立即吃到天数成长的问题
              现在出生时就会按当前世界天数正确计算初始词条数量
            ]]
        },
        {
            ["title"] = "补丁mod - 2026-03-24",
            ["desc"] = [[
            --------修复--------
            【怪物词条数量配置】
              修复启用该配置后，新生成怪物有时不会获得初始词条的问题

            --------新增功能--------
            【水晶小人转换概率配置】
              可自定义稀有/超稀有词条概率

            【累计保底系统】
              转换水晶小人、击杀精英怪、击杀Boss独立计数
              达到阈值必定获得稀有词条并全服通告
              计数是10的倍数时全服通告进度
              仅玩家或玩家召唤物击杀才计入保底计数

            【掉率配置】
              可自定义精英/Boss的极品附魔石、装备包裹掉率
              帮助页概率表会同步显示配置后的实际数值

            【怪物词条数量配置】
              可自定义各类怪物词条上限、基础数量、天数增长间隔

            【怪物玩家词条扩展】
              怪物可随机获得：真实伤害、血涌、概率暴击、反伤、末世、免疫中毒

            【移除词条】
              可禁用：暗影伪装、月灵伪装、免疫制裁(套装)
              附魔卷轴和水晶转换不会再生成暗影伪装、月灵伪装附魔石

            【附魔限制】
              攻击距离大于2的武器禁止附魔
              新增配置开关，可控制该限制是否生效，默认开启

            【移除部分宝藏怪】
              新增配置开关，默认移除 treasure_kps、treasure_cat_you

            【新增稀有附魔石】
              新增“破界”词条
              附魔时概率范围为1%~10%
              攻击拥有位面抵抗或混沌抵抗的怪物时，有概率使其失去抵抗10秒
              同一怪物效果结束后有10秒内置冷却
              破界状态下怪物受到的伤害减半
              成功触发时会显示破界提示和特效圈

            【精英/Boss分解装备词条】
              精英/Boss可随机获得“概率分解头/衣”词条
              命中玩家时可分解头部或身体装备，并全服通告
              分解会复用附魔分解规则，正常产出附魔石
              新增配置开关，可单独启用/禁用该词条
              概率范围可配置为1%~3%或1%~5%

            【宝藏Boss尾刀奖励调整】
              宝藏Boss死亡时，死亡点附近16范围内的玩家都可获得尾刀稀有宝石奖励
              若尾刀本身就是玩家，则保留本体原奖励，额外奖励附近其他玩家，避免重复获得双份

            --------调整--------
            【制裁效果】
              治疗压制从-90%改为-100%
              制裁buff持续时间翻倍
              相关帮助/词条文案同步调整

            【玩家词条上限调整】
              暗影护盾上限调整为30%
              伤害减免上限调整为50%
              秋季战神仅按1条词条生效
              白天/黄昏/夜晚增伤各自上限调整为100
            ]]
        },
    }

    -- 在数组开头插入补丁mod的更新记录
    for i = #patchLogs, 1, -1 do
        table.insert(TUNING.HH_UI_TEXT.UPDATE_VISION, 1, patchLogs[i])
    end
end

-- 延迟执行，确保本体mod已加载
AddSimPostInit(function()
    -- 使用延迟确保TUNING已初始化
    AddPatchUpdateLog()
end)
