-- 在本体mod的更新日志界面显示补丁mod的更新记录

local function AddPatchUpdateLog()
    if not TUNING or not TUNING.HH_UI_TEXT or not TUNING.HH_UI_TEXT.UPDATE_VISION then
        return
    end

    local patchLogs = {
        {
            ["title"] = "补丁mod - 2026-04-04(v1.14 汇总更新)",
            ["desc"] = [[
            新增怪物技能词条：连发弹幕、地刺陷阱、落雷、冰环、火阵、扇形喷火
            以及双子魔眼系技能（激光炮、五连快速冲撞、高速魔焰喷火）

            完善怪物技能系统：补充多人目标、扇形/直线预警
            原版冰阵/火阵、统一怪物技能CD倍率配置

            多轮优化技能表现与平衡：地刺、落雷、喷火
            双子魔眼技能的距离、数量、预警、视觉与伤害逻辑已整体打磨

            修复关键问题：双子魔眼技能作用域报错、运行时报错
            prefab依赖崩溃、喷火技能串线、激光炮破坏建筑等问题

            新增怪物技能词条第一批与第二批：喷吐、震击、冲撞、飞扑

            新增怪物玩家词条扩展：攻击距离、攻击速度、刺猬
            并加入对应配置开关

            将5个Boss独有词条同步开放给精英怪
            并补充独立配置开关

            新增真近战附魔石、复合普通附魔石
            开放生命附魔石随机获取
            并调整黄昏增伤附魔石为可重复附魔

            修复宝石升级：拆除法杖特殊道具升级宝石时
            恢复按当前等级*2消耗材料

            修复同类宝石升级：装备上存在多个同类宝石时
            会优先升级未满级的那一颗

            修复怪物技能词条：怪物死亡或移除后
            不会继续释放延迟/持续类技能

            调整宝石转换池：长击珠现已加入可转换宝石范围

            新增宝石“长击珠”：攻击距离+1
            仅限攻击距离小于2的手部武器镶嵌
            且宝石等级上限为1

            新增稀有附魔石“滑铲-稀”
            装备后获得为爽而虐滑铲能力（右键冲刺）

            新增中-急速附魔石：移速范围10%~40%

            修正中移速附魔石数值
            按原版步伐珠3%效果的2倍
            调整为6%

            调整宝石掉率默认值为5%
            并新增中移速附魔石
            （原版移速附魔石2倍效果）

            新增宝石掉落概率配置
            可在mod配置中调整宝石/特殊道具掉率

            修正宝石等级系统
            等级叠加后会立即刷新玩家移速等衍生属性

            修正宝石等级系统入口
            等级升级逻辑改为接管本体主镶嵌路径

            新增宝石等级系统
            同类宝石在无空槽时可升级
            升级消耗为当前等级*2
            ]]
        },
    }

    for i = #patchLogs, 1, -1 do
        table.insert(TUNING.HH_UI_TEXT.UPDATE_VISION, 1, patchLogs[i])
    end
end

AddPatchUpdateLog()
